package com.wanggt.freedom.spring.study.entity;

public class RoleBean {

	private String name;

	public RoleBean() {
		super();
	}

	public RoleBean(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
